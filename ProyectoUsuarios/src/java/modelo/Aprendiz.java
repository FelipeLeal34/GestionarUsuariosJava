public class Aprendiz {
    int documentoA, telefonoA;
    String nombreA, apellidoA, emailA;

    public int getDocumentoA() {
        return documentoA;
    }

    public void setDocumentoA(int documentoA) {
        this.documentoA = documentoA;
    }

    public int getTelefonoA() {
        return telefonoA;
    }

    public void setTelefonoA(int telefonoA) {
        this.telefonoA = telefonoA;
    }

    public String getNombreA() {
        return nombreA;
    }

    public void setNombreA(String nombreA) {
        this.nombreA = nombreA;
    }

    public String getApellidoA() {
        return apellidoA;
    }

    public void setApellidoA(String apellidoA) {
        this.apellidoA = apellidoA;
    }

    public String getEmailA() {
        return emailA;
    }

    public void setEmailA(String emailA) {
        this.emailA = emailA;
    }
    
    
    
}
